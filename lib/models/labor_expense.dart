class LaborExpense {
  final String id;
  final DateTime date;
  final String name;
  final int salary;
  final String category;
  final String subCategory;

  LaborExpense({
    required this.id,
    required this.date,
    required this.name,
    required this.salary,
    required this.category,
    required this.subCategory,
  });
}
